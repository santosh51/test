public class Smallestofthreenumber {

	public static void main(String[] args) {
		//smallest of 3 numbers
				int a=10,b=20,c=30;
				if(a<b&&a<c)
				{
					System.out.println("a is smallest"+a);
				}
				else if(b<a&&b<c)
				{
					System.out.println("b is smallest"+b);
					
				}
				else
				{
					System.out.println("c is smallest"+c);
				
				}
				

	}

}
